    <h4>Qu’est ce qu’un cookie</h4>
    <p>Il s’agit d’un petit fichier déposé sur le disque dur à l’insu de l’internaute, lors de la consultation de certains sites web, et qui conserve des informations en vue d’une connexion ultérieure.</p>
    <p>Les cookies peuvent être soit des cookies « persistants », soit des cookies « de session ». Un cookie persistant est un fichier texte envoyé par un serveur web à un navigateur web : il sera stocké par le navigateur et demeurera valide jusqu’à la date d’expiration fixée (à moins que l’utilisateur ne l’efface avant la date d’expiration).</p>
    <p>Un cookie de session expirera, lui, à la fin de la session, lorsque l’utilisateur ferme le navigateur.</p>
    <p>Sur ce site web, nous utilisons à la fois des cookies de session et des cookies persistants.</p>
