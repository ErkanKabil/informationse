    <h4>Wat is een cookie?
</h4>
    <p>Dit is een klein bestand dat zonder medeweten van de internetgebruiker op de harde schijf wordt opgeslagen bij het bezoeken van bepaalde websites en informatie bevat voor een latere verbinding.

</p>
    <p>Cookies kunnen "permanente" cookies of "sessiecookies" zijn. Een permanente cookie is een tekstbestand dat van een webserver naar een webbrowser wordt gestuurd. Het wordt opgeslagen door de browser en blijft geldig tot de opgegeven vervaldatum (tenzij de gebruiker het verwijdert vóór de vervaldatum).

</p>
    <p>Een sessiecookie vervalt aan het einde van de sessie wanneer de gebruiker de browser sluit.

</p>
    <p>We gebruiken zowel sessie- als permanente cookies op deze website.

